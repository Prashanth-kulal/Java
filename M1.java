public class M1 {
    public static void main(String[] args){
        for(int i=0;i<20;i++){
            System.out.print(i*2+" ");
        }
        System.out.println();
        for(int i=0;i<20;i++){
            System.out.print(i*2+1+" ");
        }
        System.out.println();
        for(int i=3;i<5;i++){
            for(int j=0;j<20;j++){
                System.out.print(i*j+" ");
            }
            System.out.println();
        }
        for(int i=5;i<21;i++){
            for(int j=0;j<20;j++){
                System.out.print(i*j+" ");
            }
            System.out.println();
        }
    }
    
}
